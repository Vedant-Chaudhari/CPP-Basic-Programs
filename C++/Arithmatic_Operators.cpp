#include <iostream>
using namespace std;

int main()

{
  int a = 4 ;
  int b = 5 ;

  //Arithmatic Operator

  cout << "The Value of a + b = " << a + b <<endl ;
  cout << "The Value of a - b = " << a - b <<endl ;
  cout << "The Value of a * b = " << a * b <<endl ;
  cout << "The Value of a / b = " << a / b <<endl ;
  cout << "The Value of a++ = " << a++ <<endl ;
  cout << "The Value of a-- = " << a-- <<endl ;
  cout << "The Value of ++a = " << ++a <<endl ;
  cout << "The Value of --a = " << --a <<endl ;

  return 0 ;

}
